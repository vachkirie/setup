  def show(self):
    for err in self.Errs:
      if err not in Global[self.Proj]['Regs']:
        print("-" * 70)
        print("Error: ErrLog{0} register is not available in this project".format(err))
        continue
      reg = Global[self.Proj]['Regs'][err]
      fields = Global[self.Proj]['Fields']
      print("-" * 70)
      print("{0} register content (0x{1:08x}):".format(reg['Name'], self.Errs[err]))
      for field in reg['Fields']:
        fieldname = field[0]
        print("{0:20} = 0x{1:<2x} ({2:2})".format(fieldname, fields[fieldname]['Val'], fields[fieldname]['Val']), end=" ")
        if "Descr" in fields[fieldname]:
          if (isinstance(fields[fieldname]['Descr'], dict)) and (fields[fieldname]['Val'] in fields[fieldname]['Descr']):
              print(" -> {0}".format(fields[fieldname]['Descr'][fields[fieldname]['Val']]))
          elif (isinstance(fields[fieldname]['Descr'], list)) and (len(fields[fieldname]['Descr']) > fields[fieldname]['Val']):
              print(" -> {0}".format(fields[fieldname]['Descr'][fields[fieldname]['Val']]))
          else:
            print(" -> UNDEFINED")
        else:
          print("")
